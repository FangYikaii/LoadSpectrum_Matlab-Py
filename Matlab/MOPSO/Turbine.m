function p=Turbine(D)
%【1速比】，【2变矩系数】，【3效率】，【4公称转矩】，【5lamda】
i_x=[0.191,0.361,0.473,0.515,0.547,0.564,0.577,0.618,0.634,0.684,0.712,0.755,0.86,0.891,0.945,0.971,1.003];
K_x=[1.8333488,1.5954447,1.3923755,1.3102226,1.2585171,1.2268396,1.1972029,1.1270202,1.0908653,0.993135,0.9179498,0.8586097,0.648291,0.5897816,0.4360348,0.3058353,0];
n_x=[34.98134,57.64488,65.85022,67.42084,68.90292,69.19524,69.05169,69.63363,69.20877,67.97937,65.31795,64.80477,55.72767,52.57382,41.22037,29.69643,0];
T_x=[558.6,558.2,549.5,542.1,535.3,532.1,527.9,514.3,508,482.6,461,419.5,288.6,244.4,179.5,146,106.8];
lamda_x=[3.73E-06,3.73E-06,3.67E-06,3.62E-06,3.58E-06,3.56E-06,3.53E-06,3.44E-06,3.40E-06,3.23E-06,3.08E-06,2.80E-06,1.93E-06,1.63E-06,1.20E-06,9.76E-07,7.14E-07];
rou=865;
g=9.8;
N=800:2400;
for i=1:length(i_x)
    Mb{i}=rou*g*lamda_x(i)*D^5*N.^2;
end
Me=Curve_engine(N);
M_zhuanxiangbeng=[18.324 16.786 17.496];
M_gongzuobeng=[149.022 29.780 14.748];
M_biansubeng=[65.521 50.072 20.413];
for i=1:3
    Me1{i}=Me-M_zhuanxiangbeng(i)-M_gongzuobeng(i)-M_biansubeng(i);
    for j=1:length(i_x)
        aax{i}{j}=intp(N,Me1{i},N,Mb{j});
        n_n{i}(j)=aax{i}{j}(1)*i_x(j);
        M_b{i}(j)=aax{i}{j}(2)*K_x(j);
    end
    n_n{i}=n_n{i}';
    M_b{i}=M_b{i}';
    x1=n_n{i};
    x2=M_b{i};
    %ployfit用于多项式拟合，返回的是多项式系数，从高到低依次存入p
    p{i}=polyfit(x1,x2,1);
end
for j=1:length(i_x)
    aax{4}{j}=intp(N,Me,N,Mb{j});
    n_n{4}(j)=aax{4}{j}(1)*i_x(j);
    M_b{4}(j)=aax{4}{j}(2)*K_x(j);
end
n_n{4}=n_n{4}';
M_b{4}=M_b{4}';
x1=n_n{4};
x2=M_b{4};
%ployfit用于多项式拟合，返回的是多项式系数，从高到低依次存入p
p{4}=polyfit(x1,x2,1);
end